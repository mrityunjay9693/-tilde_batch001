# Write a program that takes input tempearture in celcius and convert Celcius to Fah
#Celcius to fah formula : (celcius * 1.8)+32

celcius = int(input("Please provide temperature in Celcius: "))
fahrenheit = (celcius*1.8) + 32
#print(type(fahrenheit))
print("Temperature in Fahrenheit")
print(fahrenheit)



